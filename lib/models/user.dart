class User {
  int id;
  String name;
  String mobile;
  String password;
  bool usertype;
  String block;
  String postal;
  String unit;
  double lat;
  double long;
  int exp;
  User({
    required this.id,
    required this.name,
    required this.mobile,
    required this.password,
    required this.usertype,
    required this.block,
    required this.postal,
    required this.unit,
    required this.lat,
    required this.long,
    required this.exp,
  });
}
